<h1 style="text-align:center;background-color:lightblue;color:white;border-radius:6px;padding:2px;">Improvement of Potters’ Wheel</h1>

<b style="font-size:16px;">Existing System:</b>
<br>
<p style="font-size:16px;">Across India, a vast and diverse country often referred to as a sub-continent, the ancient, living tradition of pottery is evident in many archaeological finds of terracotta and pottery dating back to even the pre-Christian era. Traditional potters continue to carry forward their time honoured craft, practicing age old methods with skills passed down from a father to his son and creating articles in the same fashion as their forefathers did. Presently many artisans suffer from severe back pain after long years of working. This back pain is apparent among the potter’s community. 
</p><br><br>
<b style="font-size:16px;">Major development initiatives:</b>
<ul>
<li style="font-size:16px;">	Pedal-driven Potter’s Wheel
The wheel is based on a revolutionary concept using the bicycle pedal as the source of motion. Moulding and driving the wheel are carried out simultaneously. It is suitable for small size products. Location of seat, height of the table, position and inclination of backrest and layout of water jug were decided on ergonomic considerations. Some of them have dimensions adjustable to the body dimensions of the operator. </li><br>
<li style="font-size:16px;">	Motorized Potter’s Wheel
It is an electrical motor driven potter’s wheel. It can be used for large size pots. The operator can have a ergonomically safer working posture The output is almost 10 times that of the existing manual ones.</li>
</ul>
